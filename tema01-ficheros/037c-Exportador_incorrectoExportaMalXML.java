    private void btElegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btElegirActionPerformed
        final JFileChooser fc = new JFileChooser();
        int option = fc.showOpenDialog(this);
        nameFile = (fc.getSelectedFile().getPath());

        final int BUFFER_SIZE = 54;
        FileInputStream myFile = null;

        try {
            myFile = new FileInputStream(new File(nameFile));
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Exportador.class.getName()).log(Level.SEVERE, null, ex);
        }
        byte[] buffer = new byte[BUFFER_SIZE];
        try {
            myFile.read(buffer, 0, BUFFER_SIZE);
        } catch (IOException ex) {
            Logger.getLogger(Exportador.class.getName()).log(Level.SEVERE, null, ex);
        }

        int heigth = 0;
        int width = 0;
        File file = new File(nameFile);
        double size = file.length();

        /*if(buffer[18] < 0){
            width = buffer[18] + 256; 
        }
        else{
            heigth = buffer[18];
        }
        heigth += (buffer[19] * 256);*/
        
        width = buffer[19] * 256 + buffer[18];
        if (buffer[18] < 0) {
             width += 256;
        }
        
        heigth = buffer[23] * 256 + buffer[22];
        if (buffer[22] < 0) {
            heigth += 256;
        }
        
        
        boolean isBMP = false;

        if ((buffer[0] == 'B') && (buffer[1] == 'M')) {
            isBMP = true;
        }

        if (isBMP) {
            lbName.setText(file.getName());
            lbWidth.setText("" + width + "");
            lbHeigth.setText("" + heigth + "");
            lbSize.setText("" + size + " kb");
        }
    }//GEN-LAST:event_btElegirActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        OutputStream outputStream;
        try {
            File file = new File("generatedXML.xml");
            outputStream = new FileOutputStream("generatedXML.xml", true);

            XMLStreamWriter out = XMLOutputFactory.newInstance().createXMLStreamWriter(
                    new OutputStreamWriter(outputStream, "utf-8"));

            if (file.exists()) {
                out.writeCharacters("\n");
                out.writeStartElement("ficheros");

                out.writeCharacters("\n");
                out.writeStartElement("fichero");

                out.writeCharacters("\n");
                out.writeStartElement("nombre");
                out.writeCharacters(lbName.getText());
                out.writeEndElement();
                out.writeCharacters("\n");
                out.writeStartElement("ancho");
                out.writeCharacters(lbWidth.getText());
                out.writeEndElement();
                out.writeCharacters("\n");
                out.writeStartElement("alto");
                out.writeCharacters(lbHeigth.getText());
                out.writeEndElement();
                out.writeCharacters("\n");
                out.writeStartElement("tamaÃ±o");
                out.writeCharacters(lbSize.getText());
                out.writeEndElement();
                out.writeCharacters("\n");
                out.writeEndElement();
                out.writeCharacters("\n");
                out.writeEndElement();

                out.writeEndDocument();
                out.flush();
            } else {
                out.writeStartDocument();
            }
            out.close();

            PrintWriter printwriter = new PrintWriter(new BufferedWriter(new FileWriter("generatedCSV.csv", true)));
            printwriter.append(/*"Nombre" +*/ lbName.getText()
                    + "," + lbWidth.getText() + "," + lbHeigth.getText() + ","
                    + lbSize.getText());
            printwriter.println();
            printwriter.close();

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Exportador.class.getName()).log(Level.SEVERE, null, ex);
        } catch (XMLStreamException ex) {
            Logger.getLogger(Exportador.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(Exportador.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Exportador.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
